#!/usr/bin/env node
// scripts/generate-manifest.js
// Walks the icons/ directory and writes:
//   icons/manifest.json  — lightweight JSON (filenames only, for reference)
//   icons/manifest.js    — JS file setting window.ICON_MANIFEST with embedded SVG data URIs
//                          so the app works under file:// with no server/fetch required
// Run: node scripts/generate-manifest.js

const fs   = require('fs');
const path = require('path');

const ICONS_DIR = path.join(__dirname, '..', 'icons');
const OUT_JSON  = path.join(ICONS_DIR, 'manifest.json');
const OUT_JS    = path.join(ICONS_DIR, 'manifest.js');

const manifest     = {}; // filenames only  (for manifest.json)
const manifestData = {}; // with data URIs  (for manifest.js)

const providers = fs.readdirSync(ICONS_DIR, { withFileTypes: true })
  .filter(d => d.isDirectory())
  .map(d => d.name)
  .sort();

for (const provider of providers) {
  const providerPath = path.join(ICONS_DIR, provider);
  manifest[provider]     = {};
  manifestData[provider] = {};

  const categories = fs.readdirSync(providerPath, { withFileTypes: true })
    .filter(d => d.isDirectory())
    .map(d => d.name)
    .sort();

  for (const category of categories) {
    const categoryPath = path.join(providerPath, category);
    const files = fs.readdirSync(categoryPath, { withFileTypes: true })
      .filter(f => f.isFile() && f.name.toLowerCase().endsWith('.svg'))
      .map(f => f.name)
      .sort();

    if (files.length === 0) continue;

    manifest[provider][category] = files;
    manifestData[provider][category] = files.map(filename => {
      const filePath = path.join(categoryPath, filename);
      const svgContent = fs.readFileSync(filePath, 'utf8');
      const b64 = Buffer.from(svgContent).toString('base64');
      return {
        name: filename,
        data: `data:image/svg+xml;base64,${b64}`,
      };
    });
  }
}

fs.writeFileSync(OUT_JSON, JSON.stringify(manifest, null, 2));
fs.writeFileSync(OUT_JS,
  `// Auto-generated by scripts/generate-manifest.js — do not edit manually\n` +
  `// Contains embedded SVG data URIs so the app works under file:// protocol\n` +
  `window.ICON_MANIFEST = ${JSON.stringify(manifestData)};\n`
);

const totalIcons = Object.values(manifest)
  .flatMap(cats => Object.values(cats))
  .reduce((sum, files) => sum + files.length, 0);
const jsSizeKB = Math.round(fs.statSync(OUT_JS).size / 1024);

console.log(`JSON written to ${OUT_JSON}`);
console.log(`JS   written to ${OUT_JS} (${jsSizeKB} KB)`);
console.log(`Providers: ${Object.keys(manifest).join(', ')}`);
console.log(`Total icons: ${totalIcons}`);
